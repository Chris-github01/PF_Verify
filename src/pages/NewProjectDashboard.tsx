import { useState, useEffect } from 'react';
import {
  Plus,
  FolderOpen,
  X,
  FileText,
  TrendingUp,
  Building2,
  Clock,
  CheckCircle,
  Circle,
  ArrowRight
} from 'lucide-react';
import PageHeader from '../components/PageHeader';
import { supabase } from '../lib/supabase';
import { useOrganisation } from '../lib/organisationContext';

interface NewProjectDashboardProps {
  projectId: string | null;
  projectName?: string;
  allProjects: any[];
  onProjectSelect: (id: string) => void;
  onCreateProject: (name: string, client: string, reference: string) => Promise<string | null>;
  onNavigateToQuotes: () => void;
  onNavigateToMatrix: () => void;
  onNavigateToReports: () => void;
  onNavigateToLibrary: () => void;
}

interface StepStatus {
  id: string;
  name: string;
  status: 'not_started' | 'in_progress' | 'completed';
  route: string;
}

interface ProjectStats {
  quoteCount: number;
  totalValue: number;
  supplierCount: number;
  hasQuotes: boolean;
  hasReviewedItems: boolean;
  hasScopeMatrix: boolean;
  hasReports: boolean;
}

export default function NewProjectDashboard({
  projectId,
  projectName,
  allProjects,
  onProjectSelect,
  onCreateProject,
  onNavigateToQuotes,
  onNavigateToMatrix,
  onNavigateToReports,
  onNavigateToLibrary,
}: NewProjectDashboardProps) {
  const { currentOrganisation } = useOrganisation();
  const [showCreateModal, setShowCreateModal] = useState(false);
  const [newProjectName, setNewProjectName] = useState('');
  const [newProjectClient, setNewProjectClient] = useState('');
  const [newProjectReference, setNewProjectReference] = useState('');
  const [isCreating, setIsCreating] = useState(false);
  const [loading, setLoading] = useState(true);
  const [stats, setStats] = useState<ProjectStats>({
    quoteCount: 0,
    totalValue: 0,
    supplierCount: 0,
    hasQuotes: false,
    hasReviewedItems: false,
    hasScopeMatrix: false,
    hasReports: false,
  });
  const [steps, setSteps] = useState<StepStatus[]>([]);

  useEffect(() => {
    if (projectId) {
      loadProjectData();
    } else {
      setLoading(false);
    }
  }, [projectId]);

  const loadProjectData = async () => {
    if (!projectId) return;

    setLoading(true);
    try {
      const { data: quotes } = await supabase
        .from('quotes')
        .select('id, supplier_name, total_amount')
        .eq('project_id', projectId);

      const quoteCount = quotes?.length || 0;
      const totalValue = quotes?.reduce((sum, q) => sum + (q.total_amount || 0), 0) || 0;
      const supplierCount = new Set(quotes?.map(q => q.supplier_name)).size;

      const { data: settings } = await supabase
        .from('project_settings')
        .select('*')
        .eq('project_id', projectId)
        .maybeSingle();

      const { data: reportsList } = await supabase
        .from('award_reports')
        .select('id')
        .eq('project_id', projectId)
        .eq('status', 'ready');

      const newStats: ProjectStats = {
        quoteCount,
        totalValue,
        supplierCount,
        hasQuotes: quoteCount > 0,
        hasReviewedItems: settings?.review_clean_completed || false,
        hasScopeMatrix: settings?.scope_matrix_completed || false,
        hasReports: (reportsList?.length || 0) > 0,
      };

      setStats(newStats);
      updateStepStatuses(newStats);
    } catch (error) {
      console.error('Error loading project data:', error);
    } finally {
      setLoading(false);
    }
  };

  const updateStepStatuses = (projectStats: ProjectStats) => {
    const updatedSteps: StepStatus[] = [
      {
        id: 'import',
        name: 'Import Quotes',
        status: projectStats.hasQuotes ? 'completed' : 'not_started',
        route: 'quotes'
      },
      {
        id: 'review',
        name: 'Review & Clean',
        status: projectStats.hasReviewedItems ? 'completed' : projectStats.hasQuotes ? 'in_progress' : 'not_started',
        route: 'review-clean'
      },
      {
        id: 'queue',
        name: 'Review Queue',
        status: projectStats.hasReviewedItems ? 'completed' : 'not_started',
        route: 'review-queue'
      },
      {
        id: 'intelligence',
        name: 'Quote Intelligence',
        status: projectStats.hasReviewedItems ? 'completed' : 'not_started',
        route: 'quote-intelligence'
      },
      {
        id: 'matrix',
        name: 'Scope Matrix',
        status: projectStats.hasScopeMatrix ? 'completed' : projectStats.hasReviewedItems ? 'in_progress' : 'not_started',
        route: 'scope-matrix'
      },
      {
        id: 'reports',
        name: 'Reports',
        status: projectStats.hasReports ? 'completed' : 'not_started',
        route: 'reports'
      },
    ];
    setSteps(updatedSteps);
  };

  const handleNavigateToStep = (route: string) => {
    switch (route) {
      case 'quotes':
        onNavigateToQuotes();
        break;
      case 'scope-matrix':
        onNavigateToMatrix();
        break;
      case 'reports':
        onNavigateToReports();
        break;
      case 'library':
        onNavigateToLibrary();
        break;
      default:
        console.log('Navigation to', route, 'not yet implemented');
    }
  };

  const handleCreateClick = () => {
    setShowCreateModal(true);
  };

  const handleCloseModal = () => {
    setShowCreateModal(false);
    setNewProjectName('');
    setNewProjectClient('');
    setNewProjectReference('');
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!newProjectName.trim()) return;

    setIsCreating(true);
    const projectId = await onCreateProject(newProjectName, newProjectClient, newProjectReference);
    setIsCreating(false);

    if (projectId) {
      handleCloseModal();
    }
  };

  return (
    <div className="min-h-screen bg-gray-50">
      <div className="max-w-7xl mx-auto px-6 py-6">
        {projectId && (
          <div className="mb-3">
            <div className="text-xs text-gray-500 mb-1">Projects &gt; {projectName}</div>
          </div>
        )}
        <PageHeader
          title={projectId ? projectName || 'Project Dashboard' : 'All Projects'}
          subtitle={projectId ? 'Manage your quote analysis workflow' : 'Select or create a project'}
        />

        {!projectId ? (
          <div className="mt-6">
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
              <button
                onClick={handleCreateClick}
                className="p-6 border-2 border-dashed border-gray-300 rounded-xl hover:border-blue-500 hover:bg-blue-50 transition-colors"
              >
                <Plus className="mx-auto mb-2 text-gray-400" size={32} />
                <div className="font-medium">Create New Project</div>
              </button>

              {allProjects.map((project) => (
                <button
                  key={project.id}
                  onClick={() => onProjectSelect(project.id)}
                  className="p-6 bg-white border border-gray-200 rounded-xl hover:border-blue-500 hover:shadow-lg transition-all text-left"
                >
                  <FolderOpen className="mb-2 text-blue-600" size={24} />
                  <div className="font-semibold text-gray-900 mb-1">{project.name}</div>
                  <div className="text-sm text-gray-500">{project.client_reference}</div>
                </button>
              ))}
            </div>

            {showCreateModal && (
          <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center z-50">
            <div className="bg-white rounded-lg shadow-xl p-6 w-full max-w-md">
              <div className="flex items-center justify-between mb-4">
                <h2 className="text-xl font-bold text-gray-900">Create New Project</h2>
                <button
                  onClick={handleCloseModal}
                  className="text-gray-400 hover:text-gray-600 transition-colors"
                >
                  <X size={24} />
                </button>
              </div>

              <form onSubmit={handleSubmit}>
                <div className="space-y-4">
                  <div>
                    <label htmlFor="projectName" className="block text-sm font-medium text-gray-700 mb-1">
                      Project Name <span className="text-red-500">*</span>
                    </label>
                    <input
                      id="projectName"
                      type="text"
                      value={newProjectName}
                      onChange={(e) => setNewProjectName(e.target.value)}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-transparent bg-white text-gray-900"
                      placeholder="Enter project name"
                      required
                      autoFocus
                    />
                  </div>

                  <div>
                    <label htmlFor="projectClient" className="block text-sm font-medium text-gray-700 mb-1">
                      Client Name
                    </label>
                    <input
                      id="projectClient"
                      type="text"
                      value={newProjectClient}
                      onChange={(e) => setNewProjectClient(e.target.value)}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-transparent bg-white text-gray-900"
                      placeholder="Enter client name"
                    />
                  </div>

                  <div>
                    <label htmlFor="projectReference" className="block text-sm font-medium text-gray-700 mb-1">
                      Project Reference
                    </label>
                    <input
                      id="projectReference"
                      type="text"
                      value={newProjectReference}
                      onChange={(e) => setNewProjectReference(e.target.value)}
                      className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-transparent bg-white text-gray-900"
                      placeholder="Enter project reference"
                    />
                  </div>
                </div>

                <div className="flex gap-3 mt-6">
                  <button
                    type="button"
                    onClick={handleCloseModal}
                    className="flex-1 px-4 py-2 border border-gray-300 rounded-lg text-gray-700 hover:bg-gray-50 transition-colors"
                    disabled={isCreating}
                  >
                    Cancel
                  </button>
                  <button
                    type="submit"
                    className="flex-1 px-4 py-2 bg-blue-600 text-white rounded-lg hover:bg-blue-700 transition-colors disabled:bg-gray-400 disabled:cursor-not-allowed"
                    disabled={isCreating || !newProjectName.trim()}
                  >
                    {isCreating ? 'Creating...' : 'Create Project'}
                  </button>
                </div>
              </form>
            </div>
          </div>
            )}
          </div>
        ) : loading ? (
          <div className="flex items-center justify-center py-12">
            <div className="text-gray-400">Loading project dashboard...</div>
          </div>
        ) : (
          <div className="mt-3 space-y-3">
            <div className="flex items-center justify-between py-2 px-4 bg-white border-b border-gray-200">
              <div className="flex items-center gap-2 text-[13px] text-gray-600">
                <Building2 size={14} />
                <span>{currentOrganisation?.name || 'Loading...'}</span>
              </div>
              <div className="flex items-center gap-2 text-[13px] text-gray-500">
                <Clock size={13} />
                <span>Last updated: {new Date().toLocaleDateString()}</span>
              </div>
            </div>

            <div className="bg-white rounded-lg border border-gray-200 p-4">
              <div className="grid grid-cols-1 md:grid-cols-3 gap-3">
                <div className="flex items-center gap-3 py-2 px-3 bg-gray-50 rounded-lg">
                  <div className="p-2 bg-blue-50 rounded">
                    <FileText className="text-blue-600" size={18} />
                  </div>
                  <div>
                    <div className="text-[13px] text-gray-600 font-medium">Quotes</div>
                    <div className="text-2xl font-bold text-gray-900">{stats.quoteCount}</div>
                    <div className="text-[11px] text-gray-500">
                      {stats.supplierCount} {stats.supplierCount === 1 ? 'supplier' : 'suppliers'}
                    </div>
                  </div>
                </div>

                <div className="flex items-center gap-3 py-2 px-3 bg-gray-50 rounded-lg">
                  <div className="p-2 bg-green-50 rounded">
                    <TrendingUp className="text-green-600" size={18} />
                  </div>
                  <div>
                    <div className="text-[13px] text-gray-600 font-medium">Total Value</div>
                    <div className="text-2xl font-bold text-gray-900">
                      ${stats.totalValue.toLocaleString(undefined, { minimumFractionDigits: 0, maximumFractionDigits: 0 })}
                    </div>
                    <div className="text-[11px] text-gray-500">Combined quote value</div>
                  </div>
                </div>

                <div className="flex items-center gap-3 py-2 px-3 bg-gray-50 rounded-lg">
                  <div className="p-2 bg-blue-50 rounded">
                    <FolderOpen className="text-blue-600" size={18} />
                  </div>
                  <div>
                    <div className="text-[13px] text-gray-600 font-medium">Progress</div>
                    <div className="text-2xl font-bold text-gray-900">
                      {steps.filter(s => s.status === 'completed').length}/{steps.length}
                    </div>
                    <div className="text-[11px] text-gray-500">Steps completed</div>
                  </div>
                </div>
              </div>
            </div>

            <div className="bg-white rounded-lg border border-gray-200">
              <div className="px-4 py-3 border-b border-gray-200">
                <h2 className="text-[20px] font-semibold text-gray-900">Project Workflow</h2>
              </div>
              <div>
                {steps.map((step, index) => {
                  const isCompleted = step.status === 'completed';
                  const isClickable = !isCompleted;

                  return (
                    <div
                      key={step.id}
                      onClick={isClickable ? () => handleNavigateToStep(step.route) : undefined}
                      className={`w-full flex items-center gap-3 py-3 px-4 border-b border-gray-100 last:border-b-0 ${
                        isClickable ? 'cursor-pointer hover:bg-gray-50 group' : 'cursor-default'
                      } transition-colors`}
                    >
                      {isCompleted ? (
                        <CheckCircle size={16} className="text-green-600 flex-shrink-0" />
                      ) : (
                        <div className="flex items-center justify-center w-6 h-6 rounded-full bg-gray-100 text-[11px] font-semibold text-gray-600 group-hover:bg-blue-100 group-hover:text-blue-600 transition-colors flex-shrink-0">
                          {index + 1}
                        </div>
                      )}
                      <div className="flex items-center gap-2 flex-1 min-w-0">
                        {step.status === 'in_progress' ? (
                          <Circle className="text-blue-600 fill-blue-100 flex-shrink-0" size={18} />
                        ) : step.status === 'not_started' ? (
                          <Circle className="text-gray-400 flex-shrink-0" size={18} />
                        ) : null}
                        <div className="flex-1 min-w-0">
                          <div className="font-medium text-gray-900 text-[15px]">{step.name}</div>
                          <div className="text-[11px] text-gray-500 capitalize">{step.status.replace('_', ' ')}</div>
                        </div>
                      </div>
                      {isClickable && (
                        <ArrowRight className="text-gray-400 group-hover:text-blue-600 transition-colors flex-shrink-0" size={14} />
                      )}
                    </div>
                  );
                })}
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
}
